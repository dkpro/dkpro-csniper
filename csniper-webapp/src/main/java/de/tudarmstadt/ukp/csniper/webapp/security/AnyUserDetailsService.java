package de.tudarmstadt.ukp.csniper.webapp.security;

import static java.util.Arrays.asList;

import org.springframework.dao.DataAccessException;
import org.springframework.security.core.authority.GrantedAuthorityImpl;
import org.springframework.security.core.userdetails.User;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;

public class AnyUserDetailsService
	implements UserDetailsService
{
	@Override
	public UserDetails loadUserByUsername(String aUsername)
		throws UsernameNotFoundException, DataAccessException
	{
		return new User(aUsername, aUsername, true, true, true, true, asList(
				new GrantedAuthorityImpl("ROLE_USER")));
	}
}
